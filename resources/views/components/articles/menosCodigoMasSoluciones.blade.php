<section>
    <h2 data-i18n="articles.scaling.title">Menos picar codigo, mas soluciones que escalan</h2>
    <p data-i18n="articles.scaling.intro">
        Cuanto más aprendo, más claro veo que escalar no consiste en producir más líneas de código, sino en resolver mejor los problemas adecuados.
    </p>
    <ul>
        <li data-i18n="articles.scaling.item_1"><strong>Empieza por el problema:</strong> Antes de programar, aclara objetivo, restricciones y criterio de éxito.</li>
        <li data-i18n="articles.scaling.item_2"><strong>Diseña arquitectura simple:</strong> Una solución clara suele rendir mejor que una brillante pero difícil de mantener.</li>
        <li data-i18n="articles.scaling.item_3"><strong>Automatiza lo repetible:</strong> Tests, validaciones y procesos previsibles reducen errores evitables.</li>
        <li data-i18n="articles.scaling.item_4"><strong>Mide antes de optimizar:</strong> El rendimiento útil se mejora con datos, no con intuiciones.</li>
        <li data-i18n="articles.scaling.item_5"><strong>Piensa en equipo:</strong> Documentación, legibilidad y convenciones ahorran tiempo a todos.</li>
    </ul>
    <p data-i18n="articles.scaling.note">
        Ese es el tipo de desarrollo en el que quiero seguir creciendo: menos ruido y más impacto real.
    </p>
</section>
